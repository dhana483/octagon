
export class Information {
  public firstName: string;
  public lastName: string;
  public email: string;
  public state: string;
  public zip: number;
  constructor(firstName: string, lastName: string, email: string, state: string, zip: number){
    this.firstName = firstName;
    this.lastName = lastName;
    this.email = email;
    this.state = state;
    this.zip = zip;
  }
}

